
create table if not exists public.order_items (
    id bigint generated by default as identity,
    order_id text not null,
    product_id uuid not null,
    quantity integer not null,
    price numeric not null,
    constraint order_items_pkey primary key (id),
    constraint order_items_order_id_fkey foreign key (order_id) references orders (id) on update cascade on delete cascade,
    constraint order_items_product_id_fkey foreign key (product_id) references products (id) on update cascade on delete restrict
) tablespace pg_default;

    